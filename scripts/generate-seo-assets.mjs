import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { loadEnv } from 'vite';
import { buildCanonicalUrl, normalizeBaseUrl } from '../src/seo/siteUrl.js';

const cwd = process.cwd();
const env = loadEnv('production', cwd, '');
const baseUrl = normalizeBaseUrl(env.VITE_SITE_BASE_URL || process.env.VITE_SITE_BASE_URL);
const lastmod = new Date().toISOString().slice(0, 10);

async function getPublicCoursePaths() {
  const coursesDir = path.join(cwd, 'src', 'courses');
  const courseFiles = (await readdir(coursesDir))
    .filter((entry) => entry.endsWith('Course.js'))
    .sort();

  const routes = [];

  for (const courseFile of courseFiles) {
    const courseSource = await readFile(path.join(coursesDir, courseFile), 'utf8');
    const statusMatch = courseSource.match(/status:\s*'([^']+)'/);
    const slugMatch = courseSource.match(/slug:\s*'([^']+)'/);

    if (!slugMatch) {
      throw new Error(`Could not find slug in ${courseFile}`);
    }

    if (statusMatch && statusMatch[1] !== 'live') {
      continue;
    }

    routes.push(`/courses/${slugMatch[1]}`);
  }

  return routes;
}

function renderSitemap(urls) {
  const entries = urls.map((url) => {
    return [
      '  <url>',
      `    <loc>${url}</loc>`,
      `    <lastmod>${lastmod}</lastmod>`,
      '  </url>',
    ].join('\n');
  }).join('\n');

  return [
    '<?xml version="1.0" encoding="UTF-8"?>',
    '<!-- Generated by scripts/generate-seo-assets.mjs -->',
    '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">',
    entries,
    '</urlset>',
    '',
  ].join('\n');
}

function renderRobots() {
  return [
    '# Generated by scripts/generate-seo-assets.mjs',
    'User-agent: *',
    'Allow: /',
    '',
    `Sitemap: ${buildCanonicalUrl('/sitemap.xml', baseUrl)}`,
    '',
  ].join('\n');
}

async function main() {
  const publicDir = path.join(cwd, 'public');
  const coursePaths = await getPublicCoursePaths();
  const urls = ['/', ...coursePaths].map((routePath) => buildCanonicalUrl(routePath, baseUrl));

  await mkdir(publicDir, { recursive: true });
  await writeFile(path.join(publicDir, 'robots.txt'), renderRobots(), 'utf8');
  await writeFile(path.join(publicDir, 'sitemap.xml'), renderSitemap(urls), 'utf8');
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
